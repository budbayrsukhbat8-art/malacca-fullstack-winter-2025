public class Variables { 

    public static void main(String[] args) {
        String name = "Bold";
        int age = 25;
        double height = 1.75;
        char grade = 'A';
        boolean isActive = true;
        System.out.println("Bold:"+ name);
        System.out.println("25:"+ age);
        System.out.println("1.75"+ height);
        System.out.println("A:"+ grade);
        System.out.println("true:"+ isActive);

        
    }
}

 
    

